<h2>Informationen</h2>
<p>
	Pong ist wie Tennis mit optimierter Optik. Sie und Ihr gegner Spielen sich einen Ball zu.
	Wenn ein Spieler den Ball nicht trifft bekommt sein Gegner einen Punkt.
</p>

<h3>Spielanleitung</h3>
<p>
	<h4>1. Spiel starten</h4>
	<ul>
		<li>
			Als erstes m&uuml;ssen Sie "Game-Host" angeben. Dies ist die URL des Servers bzw. der API
			&uuml;ber welche das Spiel l&auml;uft. Hier k&ouml;nnen Sie den Standardwert, welcher bereits eingetragen ist stehen lassen.
			Wenn Sie einen anderen Server kennen, welcher die selbe Schnittstelle bereitstellt, so k&ouml;nnen Sie auch diesen verwenden. 
		</li>
		<li>
			In das Feld "Dein Name" gibst du einfach deinen Namen ein.
		</li>
		<li>
			Nun kommt es darauf an wie bzw. gegen wen du spielen m&ouml;chtest:
			<ul>
				<li>
					<strong>gegen einen unbekannten Spieler</strong><br/>
					Dr&uuml;cke einfach den Button "Gegner suchen". 
					Sobald ein Anderer ebenfalls diesen Button dr&uuml;ckt oder bereits gedr&uuml;ckt hat, 
					wird er mit dir Verbunden und das Spiel beginnt.
				</li>
				<li>
					<strong>gegen einen befreundeten Spieler</strong><br/>
					Dein Freund und Du m&uuml;sst nur den selben "Game-Host" und "Game-Key" eintragen und auf "Spiel starten" dr&uuml;cken.
				</li>
				<li>
					<strong>gegen einen befreundeten Spieler, am selben PC</strong><br/>
					Dr&uuml;ckt den Button "2 Spieler". Nun m&uuml;sst Ihr euche Namen angeben und auf "Spiel starten" dr&uuml;cken. Und los gehts.
				</li>
			</ul>
		</li>
	</ul>
	
	<h4>2. Die Steuerung</h4>
	<ul>
		<li>
			Du kannst das Paddel mit klick auf die Pfeil-Icons unter dem Spielfeld steuern,
			oder mit den Pfeiltasten auf deiner Tastatur.
		</li>
		<li>
			Wenn Ihr zu zweit im selben Browser spielt, dann kann dein Kumpel sein Paddel &uuml;ber die beiden anderen Symbole steuern und mit den Buchstaben "w" und "s" auf der Tastatur.
		</li>
	</ul>
	<h4>3. Das Spielende</h4>
	<ul>
		<li>
			Das Spiel ist zu Ende, sobald du oder dein Gegner 10 Punkte habt.
		</li>
		<li>
			Im Anschluss erfolgt eine Beldung, welcher Spiler gewonnen hat. Unter der Nachricht ist ein Button "Neues Spiel".
			Wenn du diesen dr&uuml;ckst startet das Siel neu.
		</li>
		<li>
			Du kannst auch auf das Pong-Logo im Header dr&uuml;cken und kommst zur&uuml;ck zur Startseite.
		</li>
	</ul>
</p>

